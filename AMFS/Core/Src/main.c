/*
 * Callback function execute when timer period elapsed. This function
 * is defined in the driver file as weak.
 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  /* Prevent unused argument(s) compilation warning */
  UNUSED(htim);

  /*
   * Verifies that interrupt was generated by timer 6.
   *
   */
  if (htim->Instance == TIM6)
  {
	  tim6IntCounter++;

	  if (tim6IntCounter >= 100)
	  {
		  tim6IntCounter = 0;

		  // Testing purposes.
		  testValue++;

		  if (testValue >= 9999)
			  testValue = 0;
//		  AMFS.digitValueIndex++;
	  }
  }

  // Timer used by the 7 segments display to refresh the data on the screen.
  // Every 4ms an interrupt is generated and the next digit 7 segments display
  // is updated.
  if (htim->Instance == TIM13)
  {
	  // Enable the update of the 7 segments display.
	  update7SD();
  }

}
